import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
export const notify = (message: string) => {
  toast(message, {
    position: "top-center",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: false,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
};
